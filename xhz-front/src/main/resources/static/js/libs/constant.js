	/**
	 * 翻译日期
	 * @param date
	 * @param fmt
	 * @returns {*}
	 */
	transDate : function(date, fmt) {
	    if (date) {
	        if (typeof date == 'string') {
	            return new Date(date).dateFormat(fmt);
	        } else {
	            try {
	                return new Date(date.replace('-', '/').replace('-', '/')).dateFormat(fmt);
	            } catch (e) {
	                return '-';
	            }
	        }
	    } else {
	        return '';
	    }
	},
